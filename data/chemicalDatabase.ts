// Helper Functions
export const getChemicals = () => chemicals;

export const identifyChemical = (query: string): Chemical | undefined => {
  const lowerQ = query.toLowerCase();
  return chemicals.find(c => 
    c.name.toLowerCase() === lowerQ || 
    c.commonName.toLowerCase() === lowerQ ||
    c.formula.toLowerCase() === lowerQ
  );
};

export const searchChemicals = (query: string): Chemical[] => {
  const lowerQ = query.toLowerCase();
  if (!lowerQ) return chemicals;
  
  return chemicals.filter(c => 
    c.name.toLowerCase().includes(lowerQ) || 
    c.commonName.toLowerCase().includes(lowerQ) ||
    c.formula.toLowerCase().includes(lowerQ) ||
    c.description.toLowerCase().includes(lowerQ)
  );
};

export const getChemicalsBySubject = (subject: LabType): Chemical[] => {
  return chemicals.filter(c => c.subject === subject);
};

export const checkCompatibility = (chem1: Chemical, chem2: Chemical): { compatible: boolean; warning?: string } => {
  // Check chem1's incompatibles
  const conflict1 = chem1.incompatibleWith.find(i => 
    i.toLowerCase().includes(chem2.name.toLowerCase()) || 
    i.toLowerCase().includes(chem2.commonName.toLowerCase())
  );
  
  if (conflict1) return { compatible: false, warning: `DANGER: ${chem1.commonName} is incompatible with ${chem2.commonName}!` };

  // Check chem2's incompatibles
  const conflict2 = chem2.incompatibleWith.find(i => 
    i.toLowerCase().includes(chem1.name.toLowerCase()) || 
    i.toLowerCase().includes(chem1.commonName.toLowerCase())
  );
  
  if (conflict2) return { compatible: false, warning: `DANGER: ${chem2.commonName} is incompatible with ${chem1.commonName}!` };

  return { compatible: true };
};